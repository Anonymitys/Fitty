package com.ekkoe.fitty.data

import kotlinx.serialization.Serializable

@Serializable
data class Hierarchy(
    val children: List<Sub>,
    val courseId: Int,
    val id: Int,
    val name: String,
    val order: Int,
    val parentChapterId: Int,
    val userControlSetTop: Boolean,
    val visible: Int
)

@Serializable
data class Sub(
    val courseId: Int,
    val id: Int,
    val name: String,
    val order: Int,
    val parentChapterId: Int,
    val userControlSetTop: Boolean,
    val visible: Int
)